export function generateEdges() {
  const edges = [];
  const seen = new Set();
  for (const [lineId, route] of Object.entries(LINE_ROUTES)) {
    for (let i = 0; i < route.length - 1; i++) {
      const key = [route[i], route[i+1]].sort().join('--') + '--' + lineId;
      if (!seen.has(key)) {
        seen.add(key);
        edges.push({ from: route[i], to: route[i+1], line: lineId });
      }
    }
  }
  return edges;
}
